/**
 * Represents different emotional states or moods.
 * 
 * An enumeration of basic moods that can be used to track or represent
 * emotional states within the application. This enum defines two moods:
 * {@link #HAPPY} and {@link #SAD}, which can be used to reflect a user's
 * or a pet's emotional state.
 * 
 * <p>This enum could be expanded in the future to include additional moods,
 * depending on the requirements of the application.</p>
 * 
 * @author Khushal
 * @version 1.0
 */
public enum Mood {
  /**
   * Represents a positive emotional state or happiness.
   */
  HAPPY,

  /**
   * Represents a negative emotional state or sadness.
   */
  SAD;
}
