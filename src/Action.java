/**
 * The {@code Action} enum represents different actions that can be performed.
 * <p>
 * This enum provides three constants:
 * <ul>
 *   <li>{@link #FEED} - Represents the action of feeding.</li>
 *   <li>{@link #PLAY} - Represents the action of playing.</li>
 *   <li>{@link #SLEEP} - Represents the action of sleeping.</li>
 * </ul>
 * </p>
 * <p>
 * Each action can be used in scenarios where a specific type of user or system 
 * behavior needs to be categorized or triggered.
 * </p>
 * 
 * @author Khushal
 * @version 1.0
 */
public enum Action {
  /**
   * Represents the action of feeding.
   */
  FEED,

  /**
   * Represents the action of playing.
   */
  PLAY,

  /**
   * Represents the action of sleeping.
   */
  SLEEP;
}
